var nilai = Math.abs(-5);
console.log(nilai); // Output: 5

var bilangan = Math.ceil(4.2);
console.log(bilangan); // Output: 5

var bilangan = Math.floor(4.7);
console.log(bilangan); // Output: 4

var bilangan = Math.round(4.5);
console.log(bilangan); // Output: 5

var nilaiMaksimum = Math.max(10, 5, 8, 12);
console.log(nilaiMaksimum); // Output: 12

var nilaiMinimum = Math.min(10, 5, 8, 12);
console.log(nilaiMinimum); // Output: 5

var hasilPangkat = Math.pow(2, 3);
console.log(hasilPangkat); // Output: 8

var akarKuadrat = Math.sqrt(25);
console.log(akarKuadrat); // Output: 5

var nilaiAcak = Math.random();
console.log(nilaiAcak); // Output: Nilai acak antara 0 dan 1 (contoh: 0.756927564)

var sudut = Math.PI / 2; // Sudut 90 derajat dalam radian
var sinus = Math.sin(sudut);
console.log(sinus); // Output: 1
